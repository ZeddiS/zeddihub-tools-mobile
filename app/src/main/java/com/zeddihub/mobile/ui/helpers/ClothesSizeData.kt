    val eu: String?, val us: String?, val uk: String?,
    val jp: String?, val au: String?, val cn: String?,
    val kr: String?, val ru: String?,
)

data class SizeChart(
    val labelKeyRes: Int,
    val rows: List<SizeRow>,
)

