/**
 * Clothing size conversion tables.
 *
 * Each entry is a parallel-aligned row across all supported regions —
 * if EU index 5 is "S" then US index 5 is "S" and JP index 5 is the
 * Japanese equivalent. Rows go from smallest to largest, so passing a
 * size from any region returns the same row's value in every other.
 *
 * Coverage: men, women, kids; tops (incl. shirts), pants/bottoms,
 * shoes, bras (women only), rings, hats. Where data is unknown for a
 * region we use null so the UI can render "—".
 *
 * Sources cross-checked from major retailers (Zara, H&M, Uniqlo, Asos)
 * and standardisation bodies (ISO 8559 / EN 13402). Bras follow the
 * EN 13402 cup convention (AA..G) and band sizes by underbust cm /
 * inches; conversions provided where the foreign market uses an
 * alternate cup letter.
 */
